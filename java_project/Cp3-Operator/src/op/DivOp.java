package op;

public class DivOp {

	public static void main(String[] args) {
		
		int num1 = 7;
		int num2 = 3;
		
		// 정수의 나누기 연산
		System.out.println("num1 / num2 = "+ num1/num2);
		System.out.println("num1 / num2 = "+ num1/3f);
		
		// 실수의 연산
		System.out.println("num1 / num2 = "+ 7f/3f);
		System.out.println("num1 / num2 = "+ (float)num1/num2);

	}

}






